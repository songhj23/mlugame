def check_answer_callback():
    q_data = questions[st.session_state.current_question_index]
    user_answer = st.session_state.user_mlu_input.strip() # Changed key

    if not user_answer:
        st.session_state.feedback_message = "MLU 값을 입력해주세요!"
        return

    try:
        user_mlu = float(user_answer)
    except ValueError:
        st.session_state.feedback_message = "유효한 숫자를 입력해주세요!"
        return

    correct_mlu = 0.0
    mlu_type_str = "MLU"

    if st.session_state.game_type == "mlu_w":
        correct_mlu = q_data["mluw"]
        mlu_type_str = "MLU-w"
    elif st.session_state.game_type == "mlu_m":
        correct_mlu = q_data["mlum"]
        mlu_type_str = "MLU-m"

    if abs(user_mlu - correct_mlu) < 0.01:
        st.session_state.feedback_message = "정답입니다!"
        st.session_state.score += 1
    else:
        st.session_state.feedback_message = "오답입니다."

    st.session_state.result_message = f"정답 {mlu_type_str}: {correct_mlu}"
    st.session_state.show_input = False
    st.session_state.show_next_button = True


def next_problem_callback():
    st.session_state.current_question_index += 1
    load_question()



