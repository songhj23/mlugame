# ──────────────────────────────────────────────────────
# 태그 정규화 (Kiwi는 불규칙 활용을 'VV-I', 'XSA-I' 등으로 태그함)
# ──────────────────────────────────────────────────────
def base_tag(tag):
    """'VV-I' → 'VV', 'XSA-I' → 'XSA' 처럼 접미사를 떼어낸 기본 태그를 반환."""
    return tag.split('-')[0]

# ──────────────────────────────────────────────────────
# 분석 기준 정의 (MLU-w, MLU-m) - 사용자 제공 기준 적용
# ──────────────────────────────────────────────────────
# 문장부호·기호 (MLU-m 계산에서 제외)
PUNCT_TAGS = {'SF', 'SP', 'SS', 'SE', 'SO', 'SW', 'SB', 'UNKNOWN'}

# 학교문법 기반 '단어' 단위 태그 정의 (MLU-w 산정용)
WORD_TAGS = {
    # 체언
    'NNG', 'NNP', 'NNB', 'NP', 'NR',
    # 용언 (어간 = 단어 1)
    'VV', 'VA', 'VX', 'VCP', 'VCN',
    # 수식언
    'MM', 'MAG', 'MAJ',
    # 독립언
    'IC',
    # 조사 (학교문법에서 단어로 인정)
    'JKS', 'JKC', 'JKG', 'JKO', 'JKB', 'JKV', 'JKQ', 'JX', 'JC',
    # 자립형식
    'SL', 'SH', 'SN',
}

# ──────────────────────────────────────────────────────
# MLU-w 분석 함수 (학교 문법 '단어' 기준) 및 MLU-m 분석 추가
# ──────────────────────────────────────────────────────
def run_mluw_analysis(text_content):
    sents = kiwi.split_into_sents(text_content)
    utterances = [s.text.strip() for s in sents if s.text.strip()]

    if not utterances:
        return None, None

    utterance_details = []
    utterance_word_counts = [] # For MLU-w
    utterance_morph_counts = [] # For MLU-m

    for i, utt in enumerate(utterances):
        tokens = kiwi.tokenize(utt)

        word_count = 0  # Based on WORD_TAGS
        morph_count = 0 # Based on non-PUNCT_TAGS

        for token in tokens:
            tag_base = base_tag(token.tag)
            if tag_base in WORD_TAGS:
                word_count += 1
            if tag_base not in PUNCT_TAGS: # MLU-m counts all morphemes, excluding punctuation
                morph_count += 1

        utterance_word_counts.append(word_count)
        utterance_morph_counts.append(morph_count)

        utterance_details.append(
            f" - 발화 {i+1}: '{utt}' (단어수: {word_count}, 형태소수: {morph_count})"
        )

    total_utterances = len(utterances)
    mlu_w_calculated = sum(utterance_word_counts) / total_utterances
    mlu_m_calculated = sum(utterance_morph_counts) / total_utterances

    mlu_w_rounded = round(mlu_w_calculated, 2)
    mlu_m_rounded = round(mlu_m_calculated, 2)

    return {
        "utterance_details": utterance_details,
        "total_utterances": total_utterances,
        "mlu_w": mlu_w_rounded,
        "mlu_m": mlu_m_rounded
    }, utterances


